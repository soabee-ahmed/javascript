//WHILE LOOP
let i=0;
while(i<=10){
    console.log(`value of i is ${i}`);
    i+=2
}
let myarr=['superman','flash','batman']
let arr=0;
while(arr<myarr.length){
    console.log(`value is ${myarr[arr]}`);
    arr=arr+1
}

//DO WHILE LOOP  :- it must execute atleast once even when the condition is false
let score =1
do{
    console.log(`score is ${score}`);
    score++
}while(score<=10)